  // Get all cheatsheets
  static List<CheatSheet> get all => allCheatsheets;

  // Get by category
  static List<CheatSheet> getByCategory(String category) {
    return allCheatsheets
        .where((sheet) => sheet.category == category)
        .toList();
  }

  // Get by difficulty
  static List<CheatSheet> getByDifficulty(String difficulty) {
    return allCheatsheets
        .where((sheet) => sheet.difficulty == difficulty)
        .toList();
  }

  // Search cheatsheets
  static List<CheatSheet> search(String query) {
    final lowerQuery = query.toLowerCase();
    return allCheatsheets.where((sheet) {
      return sheet.title.toLowerCase().contains(lowerQuery) ||
          sheet.description.toLowerCase().contains(lowerQuery) ||
          sheet.tags.any((tag) => tag.toLowerCase().contains(lowerQuery));
    }).toList();
  }

  // Get trending (most viewed)
  static List<CheatSheet> getTrending({int limit = 5}) {
    final sorted = List<CheatSheet>.from(allCheatsheets)
      ..sort((a, b) => b.viewCount.compareTo(a.viewCount));
    return sorted.take(limit).toList();
  }

  // Get favorites
  static List<CheatSheet> getFavorites() {
    return allCheatsheets.where((sheet) => sheet.isFavorite).toList();
  }

  // Get recently viewed
  static List<CheatSheet> getRecentlyViewed({int limit = 5}) {
    final viewed = allCheatsheets
        .where((sheet) => sheet.lastViewed != null)
        .toList()
      ..sort((a, b) => b.lastViewed!.compareTo(a.lastViewed!));
    return viewed.take(limit).toList();
  }

  // Get categories
  static List<String> get categories {
    return allCheatsheets
        .map((sheet) => sheet.category)
        .toSet()
        .toList()
      ..sort();
  }

  // Get quick access (popular beginner-friendly refs)
  static List<CheatSheet> getQuickAccess() {
    return [
      allCheatsheets.firstWhere((s) => s.id == 'python'),
      allCheatsheets.firstWhere((s) => s.id == 'git'),
      allCheatsheets.firstWhere((s) => s.id == 'docker'),
      allCheatsheets.firstWhere((s) => s.id == 'vscode'),
    ];
  }

  // Get count by category
  static Map<String, int> getCategoryCounts() {
    final counts = <String, int>{};
    for (var sheet in allCheatsheets) {
      counts[sheet.category] = (counts[sheet.category] ?? 0) + 1;
    }
    return counts;
  }

  // Get total count
  static int get totalCount => allCheatsheets.length;
